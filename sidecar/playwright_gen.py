import logging
import os
from datetime import datetime, timezone

import db

_SIDECAR_DIR = os.path.dirname(os.path.abspath(__file__))
_PROJECT_ROOT = os.path.dirname(_SIDECAR_DIR)
_MACROS_DIR = os.path.join(_PROJECT_ROOT, "data", "macros")

_BROWSER_NAMES = {"chrome", "firefox", "edge", "safari", "browser"}


def _ts_to_float(evt: dict) -> float:
    """Convert an event's ISO timestamp string to a Unix epoch float for delta math."""
    ts = evt.get("timestamp") or ""
    if not ts:
        return 0.0
    try:
        dt = datetime.fromisoformat(ts)
        if dt.tzinfo is None:
            dt = dt.replace(tzinfo=timezone.utc)
        return dt.timestamp()
    except (ValueError, AttributeError):
        return 0.0
_BROWSER_SUFFIXES = [
    " - Google Chrome",
    " - Mozilla Firefox",
    " - Firefox",
    " - Microsoft Edge",
    " - Safari",
]


# ---------------------------------------------------------------------------
# Primitives
# ---------------------------------------------------------------------------


def is_browser_event(event: dict) -> bool:
    app = (event.get("app_name") or "").lower()
    return any(name in app for name in _BROWSER_NAMES)


def extract_url_from_title(window_title: str) -> str | None:
    if not window_title:
        return None
    # Heuristic 1: contains "- http" — extract the URL after the dash
    idx = window_title.find("- http")
    if idx != -1:
        return window_title[idx + 2:].strip() or None
    # Heuristic 2: strip known browser suffix and return remainder if it looks like a URL
    for suffix in _BROWSER_SUFFIXES:
        if window_title.endswith(suffix):
            candidate = window_title[: -len(suffix)].strip()
            if candidate.startswith(("http://", "https://")):
                return candidate
            return None
    return None


def classify_event(event: dict) -> str:
    t = event.get("event_type", "")
    detail = event.get("detail", "") or ""

    if t == "window_focus" and is_browser_event(event):
        return "navigate"
    if t == "mouse_click":
        return "click"
    if t == "key_press":
        # Unwrap pynput quoted format: "'a'" -> "a"
        if detail.startswith("'") and detail.endswith("'") and len(detail) >= 3:
            inner = detail[1:-1]
        else:
            inner = detail
        # Special keys like Key.space, Key.shift are "unknown"; printable chars are "type"
        if inner and not inner.startswith("Key."):
            return "type"
        return "unknown"
    if t == "mouse_scroll":
        return "scroll"
    return "unknown"


def _extract_char(event: dict) -> str:
    detail = event.get("detail", "") or ""
    if detail.startswith("'") and detail.endswith("'") and len(detail) >= 3:
        return detail[1:-1]
    if len(detail) == 1:
        return detail
    return ""


def group_keystrokes(events: list[dict]) -> list[dict]:
    result: list[dict] = []
    i = 0
    while i < len(events):
        evt = events[i]
        if classify_event(evt) != "type":
            result.append(evt)
            i += 1
            continue

        # Accumulate consecutive type events within 2 seconds
        chars = [_extract_char(evt)]
        base_ts = _ts_to_float(evt)
        last_ts = base_ts
        j = i + 1

        while j < len(events):
            next_evt = events[j]
            next_ts = _ts_to_float(next_evt)
            if classify_event(next_evt) == "type" and (next_ts - last_ts) <= 2.0:
                chars.append(_extract_char(next_evt))
                last_ts = next_ts
                j += 1
            else:
                break

        merged = dict(evt)
        merged["detail"] = "".join(chars)
        merged["timestamp"] = base_ts
        result.append(merged)
        i = j

    return result


