def generate_playwright_script(events: list[dict], name: str) -> str:
    browser_events = [e for e in events if is_browser_event(e)]

    header = (
        f"# Playwright script: {name}\n"
        f"# Generated by Personal Process Miner\n"
    )

    if not browser_events:
        return (
            header
            + "# Steps: 0\n"
            + "from playwright.sync_api import sync_playwright\n"
            + "import time\n\n"
            + "# No browser events detected in this workflow\n"
        )

    grouped = group_keystrokes(browser_events)

    step_num = 0
    step_lines: list[str] = []
    prev_ts = _ts_to_float(grouped[0])

    for i, evt in enumerate(grouped):
        ts = _ts_to_float(evt)
        delta = min(ts - prev_ts, 3.0) if i > 0 else 0.0
        prev_ts = ts

        if delta > 0.01:
            step_num += 1
            step_lines.append(f"\n        # Step {step_num}: Wait")
            step_lines.append(f"        time.sleep({delta:.3f})")

        step_num += 1
        kind = classify_event(evt)

        if kind == "navigate":
            url = extract_url_from_title(evt.get("window_title") or "") or "about:blank"
            step_lines.append(f"\n        # Step {step_num}: Navigate")
            step_lines.append(f'        page.goto("{url}")')
        elif kind == "click":
            x, y = evt.get("x"), evt.get("y")
            if x is not None and y is not None:
                step_lines.append(f"\n        # Step {step_num}: Click at ({x}, {y})")
                step_lines.append(f"        page.mouse.click({x}, {y})")
        elif kind == "type":
            text = evt.get("detail", "")
            # Unwrap if still in pynput quoted format
            if text.startswith("'") and text.endswith("'") and len(text) >= 3:
                text = text[1:-1]
            text_escaped = text.replace('"', '\\"').replace("\\", "\\\\")
            step_lines.append(f"\n        # Step {step_num}: Type text")
            step_lines.append(f'        page.keyboard.type("{text_escaped}")')
        elif kind == "scroll":
            y = evt.get("y", 0) or 0
            step_lines.append(f"\n        # Step {step_num}: Scroll")
            step_lines.append(f"        page.mouse.wheel(0, {y})")
        else:
            # Unknown events are comments — don't increment step counter
            step_num -= 1
            step_lines.append(
                f"\n        # Unknown event: {evt.get('event_type', 'unknown')}"
            )

    body = "\n".join(step_lines)

    return (
        header
        + f"# Steps: {step_num}\n"
        + "from playwright.sync_api import sync_playwright\n"
        + "import time\n\n"
        + "def run():\n"
        + "    with sync_playwright() as p:\n"
        + "        browser = p.chromium.launch(headless=False)\n"
        + "        page = browser.new_page()\n"
        + body
        + "\n\n"
        + "        browser.close()\n\n"
        + 'if __name__ == "__main__":\n'
        + "    run()\n"
    )


def save_playwright_script(
    events: list[dict], name: str, workflow_id: int | None
) -> dict:
    try:
        script = generate_playwright_script(events, name)

        os.makedirs(_MACROS_DIR, exist_ok=True)
        name_slug = "".join(
            c if (c.isalnum() or c == "_") else "_"
            for c in name.lower().replace(" ", "_")
        )
        script_path = os.path.join(_MACROS_DIR, f"{name_slug}_playwright.py")

        with open(script_path, "w", encoding="utf-8") as f:
            f.write(script)

        # Count action steps
        step_count = script.count("page.")

        from datetime import datetime, timezone
        automation_id = db.insert_automation({
            "workflow_id": workflow_id,
            "name": name,
            "script_type": "playwright",
            "script_body": script,
            "created_at": datetime.now(timezone.utc).isoformat(),
        })

        return {
            "ok": True,
            "automation_id": automation_id,
            "script_path": script_path,
            "step_count": step_count,
        }
    except Exception as exc:
        logging.exception("save_playwright_script error")
        return {"ok": False, "error": str(exc)}
