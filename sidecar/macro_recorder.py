import logging
import math
import os
import threading
import time
from datetime import datetime, timezone

try:
    from pynput import keyboard, mouse
    HAS_PYNPUT = True
except Exception:
    keyboard = None
    mouse = None
    HAS_PYNPUT = False

import db

_lock = threading.Lock()
_state: dict = {
    "status": "idle",   # "idle" | "recording" | "stopped"
    "buffer": [],
    "last_mouse_pos": None,
}
_kb_listener = None
_mouse_listener = None

_SIDECAR_DIR = os.path.dirname(os.path.abspath(__file__))
_PROJECT_ROOT = os.path.dirname(_SIDECAR_DIR)
_MACROS_DIR = os.path.join(_PROJECT_ROOT, "data", "macros")

_KEY_MAP = {
    "escape": "esc",
    "page_up": "pageup",
    "page_down": "pagedown",
    "caps_lock": "capslock",
    "num_lock": "numlock",
    "scroll_lock": "scrolllock",
    "print_screen": "printscreen",
    "backspace": "backspace",
    "enter": "enter",
    "space": "space",
    "tab": "tab",
    "up": "up",
    "down": "down",
    "left": "left",
    "right": "right",
    "home": "home",
    "end": "end",
    "insert": "insert",
    "delete": "delete",
}


def _pynput_to_pyautogui(detail: str) -> str:
    """Convert pynput key string to a pyautogui key name."""
    if detail.startswith("'") and detail.endswith("'") and len(detail) >= 3:
        return detail[1:-1]
    if detail.startswith("Key."):
        name = detail[4:]
        return _KEY_MAP.get(name, name)
    return detail


